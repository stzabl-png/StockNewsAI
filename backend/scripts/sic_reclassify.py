POLYGON_BASE = "https://api.polygon.io"

async def batch_classify(tickers: list[str], api_key: str, semaphore) -> dict[str, str]:
    """Fetch SIC code for a ticker and return {ticker: sector}"""
    result = {}
    async with semaphore:
        for ticker in tickers:
            try:
                async with httpx.AsyncClient(timeout=10) as client:
                    r = await client.get(
                        f"{POLYGON_BASE}/v3/reference/tickers/{ticker}",
                        params={"apiKey": api_key}
                    )
                    if r.status_code == 200:
                        d = r.json().get("results", {})
                        sic = d.get("sic_code")
                        if sic:
                            sic_int = int(sic)
                            sector = SIC_SECTOR.get(sic_int)
                            if sector:
                                result[ticker] = sector
                    elif r.status_code == 429:
                        await asyncio.sleep(60)
            except Exception as e:
                pass
            await asyncio.sleep(0.05)  # 20 req/s stay under free tier 5req/s? use 0.2
    return result


async def main():
    # Get all 综合类 companies (excluding SPACs that start with common patterns)
    async with async_session() as session:
        rows = (await session.execute(text(
            "SELECT ticker FROM companies WHERE sector='综合类' AND is_active=true ORDER BY ticker"
        ))).fetchall()

    tickers = [r[0] for r in rows]
    print(f"🔍 待分类: {len(tickers)} 家综合类公司")

    if not settings.POLYGON_API_KEY:
        print("❌ 未配置 POLYGON_API_KEY")
        return

    # Process in batches with concurrency limit
    sem = asyncio.Semaphore(3)  # 3 concurrent
    batch_size = 30
    total_updated = 0
    sector_counts: dict[str, int] = {}

    for i in range(0, len(tickers), batch_size):
        batch = tickers[i:i+batch_size]
        updates = await batch_classify(batch, settings.POLYGON_API_KEY, sem)

        if updates:
            async with async_session() as session:
                for ticker, sector in updates.items():
                    await session.execute(
                        text("UPDATE companies SET sector=:s WHERE ticker=:t"),
                        {"s": sector, "t": ticker}
                    )
                await session.commit()
                total_updated += len(updates)
                for sector in updates.values():
                    sector_counts[sector] = sector_counts.get(sector, 0) + 1

        done = min(i + batch_size, len(tickers))
        print(f"  [{done}/{len(tickers)}] 已更新 {total_updated} 家  ", end="\r")

    print(f"\n✅ 完成! 共分类 {total_updated} 家公司")
    print("\n分类结果:")
    for sector, cnt in sorted(sector_counts.items(), key=lambda x: -x[1]):
        print(f"  {sector}: {cnt}")

if __name__ == "__main__":
    asyncio.run(main())
